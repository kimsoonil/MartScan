export function categorizeProductName(name: string): ProductCategory {
  const n = name.toLowerCase();
  for (const { category, keywords } of RULES) {
    for (const kw of keywords) {
      if (n.includes(kw.toLowerCase())) return category;
    }
  }
  return "other";
}
